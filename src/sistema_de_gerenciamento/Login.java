    void userVerification(String username, String password)
    {
        try 
        {
           Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/gestao_de_taxas","root","1234");
            String sql = "select * from paginadeincricao where username=? and password=?";
            PreparedStatement pst=con.prepareStatement(sql);
            
            pst.setString(1, username);
            pst.setString(2, password);
            ResultSet rs= pst.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "Login bem sucedido !");
                Pagina_inicial pgnInicial = new Pagina_inicial();
                pgnInicial.show();
                this.dispose();                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "O nome de usu√°rio e senha esta errado !");
            }
        } 
        catch (Exception e) 
        {
            
        }
                
    }

